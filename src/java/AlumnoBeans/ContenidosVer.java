package AlumnoBeans;

/**
 *
 * @author Maximus
 */
public class ContenidosVer {
    private String NombreGrupo;
    private String Nivel;
    private String ContenidoNombre;
    private String ContenidoID;
    private String Pagina;

    public String getNombreGrupo() {
        return NombreGrupo;
    }

    public void setNombreGrupo(String NombreGrupo) {
        this.NombreGrupo = NombreGrupo;
    }

    public String getNivel() {
        return Nivel;
    }

    public void setNivel(String Nivel) {
        this.Nivel = Nivel;
    }

    public String getContenidoNombre() {
        return ContenidoNombre;
    }

    public void setContenidoNombre(String ContenidoNombre) {
        this.ContenidoNombre = ContenidoNombre;
    }

    public String getContenidoID() {
        return ContenidoID;
    }

    public void setContenidoID(String ContenidoID) {
        this.ContenidoID = ContenidoID;
    }

    public String getPagina() {
        return Pagina;
    }

    public void setPagina(String Pagina) {
        this.Pagina = Pagina;
    }
    
}
